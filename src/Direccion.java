public class Direccion {
//Declaración de variables
	private String ciudad, calle, piso;
	private int portal, codPostal;


//Métodos getter y setter
	public String getCiudad() {
		return ciudad;
	}
	public void setCiudad(String ciudad1) {
		ciudad = ciudad1;
	}

	public String getCalle() {
		return calle;
	}
	public void setCalle(String calle1) {
		calle = calle1;
	}

	public int getPortal() {
		return portal;
	}
	public void setPortal(int portal1) {
		portal = portal1;
	}

	public String getPiso() {
		return piso;
	}
	public void setPiso(String piso1) {
		piso = piso1;
	}

	public int getCodPostal() {
		return codPostal;
	}
	public void setCodPostal(int codPostal1) {
		codPostal = codPostal1;
	}


}